package review4;

public class NestedLoops {
    public static void main(String[] args) {
        for(int i= 1; i<=3;i++){
            System.out.println("Hello");

            for(int j =1; j<3;j++){
                System.out.println("Bye");

            }

        }
        for(int i= 1; i<=3;i++){
            System.out.println("Hello");

            for(int j =1; j<3;j++){
                System.out.println("Bye");

            }

        }

    }
}
